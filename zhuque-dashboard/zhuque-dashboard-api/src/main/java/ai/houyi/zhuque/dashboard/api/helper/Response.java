package ai.houyi.zhuque.dashboard.api.helper;

/**
 * @author weiping wang
 *
 */
public class Response {
	private int code;
	private String msg;
	private Object data;

	public Response(int code, String msg, Object data) {
		this.code = code;
		this.msg = msg;
		this.data = data;
	}

	public int getCode() {
		return code;
	}

	public String getMsg() {
		return msg;
	}

	public Object getData() {
		return data;
	}
}
